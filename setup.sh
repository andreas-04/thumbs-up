#!/usr/bin/env bash
#
# ThumbsUp Host Setup Script
# Generates .env and sets up Avahi mDNS on a Debian/Raspberry Pi OS host.
#
# Usage:
#   sudo ./setup.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# ---------------------------------------------------------------------------
# Must run as root
# ---------------------------------------------------------------------------
if [[ $EUID -ne 0 ]]; then
    echo "Error: this script must be run as root (use sudo ./setup.sh)"
    exit 1
fi

# ---------------------------------------------------------------------------
# Generate .env if it doesn't exist
# ---------------------------------------------------------------------------
ENV_FILE="$SCRIPT_DIR/.env"

if [[ -f "$ENV_FILE" ]]; then
    echo "Found existing .env — loading configuration"
    set -o allexport
    # shellcheck disable=SC1090
    source "$ENV_FILE"
    set +o allexport
else
    echo
    echo "No .env found — generating default configuration."
    echo

    cat > "$ENV_FILE" <<EOF
# ThumbsUp Configuration — generated by setup.sh
# Edit this file to change settings, then run: docker compose up -d

MDNS_HOSTNAME=thumbsup
EOF

    echo ".env generated at $ENV_FILE"
    echo "To customize the mDNS hostname, edit MDNS_HOSTNAME in $ENV_FILE"
    echo
fi

MDNS_HOSTNAME="${MDNS_HOSTNAME:-thumbsup}"

echo
echo "========================================"
echo " ThumbsUp Host Setup"
echo "========================================"
echo

# ---------------------------------------------------------------------------
# 1. Install avahi-daemon
# ---------------------------------------------------------------------------
if ! command -v avahi-daemon &>/dev/null; then
    echo "Installing avahi-daemon..."
    apt-get update -qq
    apt-get install -y --no-install-recommends avahi-daemon avahi-utils
else
    echo "avahi-daemon already installed"
fi

# ---------------------------------------------------------------------------
# 2. Install the mDNS service advertisement file
# ---------------------------------------------------------------------------
AVAHI_SERVICE_SRC="$SCRIPT_DIR/config/thumbsup.avahi-service"
AVAHI_SERVICE_DST="/etc/avahi/services/thumbsup.service"

if [[ -f "$AVAHI_SERVICE_SRC" ]]; then
    cp "$AVAHI_SERVICE_SRC" "$AVAHI_SERVICE_DST"
    echo "Installed Avahi service file -> $AVAHI_SERVICE_DST"
else
    echo "Warning: $AVAHI_SERVICE_SRC not found — skipping service file install"
fi

# ---------------------------------------------------------------------------
# 3. Enable and restart avahi-daemon
# ---------------------------------------------------------------------------
systemctl enable avahi-daemon
systemctl restart avahi-daemon
echo "avahi-daemon enabled and restarted"

# ---------------------------------------------------------------------------
# Done
# ---------------------------------------------------------------------------
echo
echo "✅ Host setup complete!"
echo "   Device will be discoverable at: https://${MDNS_HOSTNAME}.local"
echo "   (Customize MDNS_HOSTNAME in .env to change the hostname)"
echo
echo "Next steps:"
echo "  docker compose up -d"
echo
